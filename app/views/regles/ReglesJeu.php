    <div class="contentBloc">
        <label class="contentTitle">
            les mises en jeu
        </label>
        <label class="contentText">
            Un arrêt de jeu arrête le temps, et le jeu doit toujours reprendre par une mise en jeu
            <br> <br> <br>
            Ce terme désigne la procédure selon laquelle un joueur de chaque équipe se tient dos à son propre but, tandis
            que les autres se tiennent à l'extérieur du cercle
            <br> <br> <br>
            L'arbitre doit déposer le palet dans le petit cercle rouge, qui est à l'intérieur du grand cercle rouge, dès qu'un des deux joueurs est prêt
            (a placé correctement sa crosse sur la glace)
            <br> <br> <br>
            Les règles sont les suivantes : le joueur en défense doit être le premier à placer sa crosse lors d'une mise en jeu dans sa propre
            zone défensive, alors que c'est l'équipe en déplacement qui est tenue de le faire lors d'une mise en jeu dans la zone neutre
            <br> <br> <br>
            L'arbitre (noir/blanc) laisse le palet tomber entre les deux joueurs qui utilisent la crosse pour prendre le palet
            <br> <br> <br>
            En général, le palet est libéré à partir du cercle de mise en jeu le plus proche sur la patinoire où il se trouvait
            avant qu'il ne passe par-dessus la bande ou qu'une faute ne soit commise par un joueur
            <br> <br> <br>
            Il y a quelques exceptions : en cas de blessure accompagnant naturellement un arrêt de jeu, l'arbitre détermine l'endroit de la glace où le palet
            doit être remis en jeu : cela dépend de son emplacement au moment de l'arrêt de jeu et de l'équipe qui était en
            possession du palet, et non de l'endroit où le joueur a été blessé sur la glace.
            <br> <br> <br>
            Si l'équipe du joueur blessé était en possession du palet dans sa zone d'attaque, la mise au jeu a lieu dans l'un des cercles situés à l'extérieur de la
            ligne bleue dans cette même zone d'attaque
            <br> <br> <br>
            Toutefois, s'ils l'ont dans la zone neutre, la mise-en-jeu est effectuée
            au cercle de la zone neutre le plus proche de l'arrêt de jeu
            <br> <br> <br>
            La même chose se produit en zone défensive s'ils l'ont dans leur propre zone défensive
            <br> <br> <br>
            Au début d'une nouvelle période, après un but ou si deux joueurs bloquent le palet près de la ligne médiane rouge, la mise au jeu se fait au cercle central
        </label>
    </div>
    <a id="ancreIcing"></a>
    <div class="contentBloc lastContentBloc">
        <label class="contentTitle">
            le icing
        </label>
        <label class="contentText">
            Si le palet est tiré depuis la moitié de la patinoire derrière la ligne de but de l'adversaire,
            l'icing est prononcé, à moins que l'équipe ne prenne possession du palet avant l'adversaire
            <br> <br> <br>
            Cette règle a été inventée pour empêcher les temps morts parce qu'un icing entraîne une mise-en-jeu
            dans la propre zone défensive en faveur de l'adversaire
            <br> <br> <br>
            Cependant, il est toujours permis de tirer le palet derrière la
            ligne de but de l'adversaire à partir d'une position dans votre propre moitié de patinoire dans une situation
            d'impuissance sans causer d'icing, car cela n'est pas considéré comme un temps mort, mais comme une tentative de défendre coûte que coûte
            <br> <br> <br>
            La situation ici est de prétendre que le joueur attaquant (orange) tire le palet derrière la ligne de but
            <br> <br> <br>
            Comme le défenseur (vert) atteint le palet en premier, l'icing est décidé
            <br> <br> <br>
            Alors qu'un icing est toujours en cours, l'arbitre de ligne doit maintenant faire deux jugements en une fraction de seconde : le
            palet a-t-il suffisamment de vitesse pour franchir la ligne de but ? Et si c'est le cas, le joueur qui défend ou
            celui qui attaque doit-il atteindre le palet en premier ? Les décisions doivent être prises et tranchées avant que
            l'attaquant n'atteigne le cercle de mise-en-jeu le plus proche de la ligne de but
            <br> <br> <br>
            S'il s'agit du joueur attaquant, le jeu continue, et s'il s'agit du joueur défendant, l'icing est décidé
            <br> <br> <br>
            S'il y a une impasse quant à la personne qui est susceptible d'atteindre le palet en premier, l’icing est appelé
        </label>
    </div>
    <a id="ancreHorsJeux"></a>
    <div class="contentBloc lastContentBloc">
        <label class="contentTitle">
            les hors jeux
        </label>
        <label class="contentText">
            Lorsqu'une équipe attaque, le palet doit d'abord traverser toute la ligne bleue de la zone d'attaque
            <br> <br> <br>
            Cela signifie que le joueur en possession du palet et ses coéquipiers doivent chronométrer leurs courses
            de manière à remplir la zone après que le palet a franchi la ligne
            <br> <br> <br>
            Que ce soit lors d'une passe à un joueur
            qui est déjà en position de hors-jeu de l'autre côté de la ligne bleue, mais aussi lorsque vous poussez vous-même
            le palet vers l'avant, vous devez être le premier à entrer en zone d'attaque
            <br> <br> <br>
            On mesure la position des patins,
            si un seul patin se trouve en zone neutre, aucun hors-jeu n'est prononcé, même si l'autre se trouve en zone d'attaque
            <br> <br> <br>
            Lorsque le meneur du palet entre dans la zone avant son coéquipier, aucun hors-jeu n'est signalé et l'attaque peut se poursuivre
            <br> <br> <br>
            La règle du hors-jeu a été inventée pour éliminer la possibilité de placer un joueur au milieu, qui est censé attendre le palet
            <br> <br> <br>
            Bien que le hors-jeu soit principalement régi dans les situations où le palet est déplacé de la zone défensive/neutre vers la zone offensive,
            un joueur peut être en attente de hors-jeu dans la zone offensive lorsqu'il y gagne le palet : le hors-jeu est alors jugé d'abord
            si le joueur zigzague vers l'arrière en zone neutre et vers l'avant à nouveau avec le palet si ses coéquipiers sont déjà en zone offensive
            <br> <br> <br>
            Décision de hors-jeu car le coéquipier entre en zone offensive avant le joueur meneur du palet (orange)
            <br> <br> <br>
            Un hors-jeu entraîne généralement une mise-en-jeu en zone médiane
        </label>
    </div>
